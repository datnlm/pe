package truonglt.dtos;

/**
 *
 * @author datnlm
 */
public class ItemDTO {

    private String id, name, point, type;

    public ItemDTO() {
    }

    public ItemDTO(String id, String name, String point, String type) {
        this.id = id;
        this.name = name;
        this.point = point;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPoint() {
        return point;
    }

    public void setPoint(String point) {
        this.point = point;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
